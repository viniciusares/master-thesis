function gA=gA(tipo246,X1,X3,D,k0)

x1=X1(1); y1=X1(2); x3=X3(1); y3=X3(2);
L=sqrt((x3-x1)^2+(y3-y1)^2);

switch tipo246
    case 1 %g11
        gA=L^3/(76800*pi*D)*(...
            -2*(5*k0*(3*k0*(k0*(3*k0-5)+1)+25)+54)+...
            15*(-3*k0^5+5*k0^4-5*k0+3)*log(-1/2*(k0-1)*L)+...
            15*(k0*(3*k0-14)+33)*(k0+1)^3*log(1/2*(k0+1)*L));
    case 2 %g22
        gA=L/(64*pi*D)*(...
            6*(k0-1)*k0^2*atanh(k0)-6*(k0-1)*k0+6*log((k0+1)*L)+1-6*log(2));
        
    case 3 %g13
        gA=L^3/(57600*pi*D)*(...
            135*k0^4-555*k0^2+105*log(-4/((k0^2-1)*L^2))+...
            15*k0*((-9*k0^4+40*k0^2-15)*atanh(k0)+...
            15*k0*log(-1/4*(k0^2-1)*L^2))-38);
    case 4 %g24
        gA=L/(32*pi*D)*(...
            (8*k0-6*k0^3)*atanh(k0)+6*k0^2+log(1/4-k0^2/4)+2*log(L)-5);
        
    case 5 %g15
        gA=L^3/(76800*pi*D)*(...
            -2*(5*k0*(3*k0*(k0*(3*k0+5)+1)-25)+54)+...
            15*(3*k0^5+5*k0^4+5*k0+3)*log(1/2*(k0+1)*L)-...
            15*(k0*(3*k0+14)+33)*(k0-1)^3*log(-1/2*(k0-1)*L));
    case 6 %g26
        gA=L/(64*pi*D)*(...
            6*(k0+1)*k0^2*atanh(k0)-6*(k0+1)*k0+6*log(1/2*(L-k0*L))+1);
end